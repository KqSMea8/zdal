/**
 * 
 * @author 伯牙
 * @version $Id: SQLUnionOperator.java, v 0.1 2012-11-17 下午3:28:17 Exp $
 */
public enum SQLUnionOperator {
    UNION("UNION"), UNION_ALL("UNION ALL"), MINUS("MINUS"), INTERSECT("INTERSECT"), DISTINCT(
                                                                                             "UNION DISTINCT");

    public final String name;

    private SQLUnionOperator(String name) {
        this.name = name;
    }

    public String toString() {
        return name;
    }
}
